# 首字母大写
name = "i love python"
print(name.title())

# 全大写以及全小写
name_2 = "Ada"
print(name_2.upper())
print(name_2.lower())

# 删除字符串中的空白
favorite_language = ' Python '
print(favorite_language.rstrip())
print(favorite_language.lstrip())
print(favorite_language.strip())

# 删除前缀
nostarch_url = 'https://nostarch.com'
print(nostarch_url.removeprefix("https://"))

